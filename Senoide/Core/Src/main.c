/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_TIM3_Init();
  MX_USART1_UART_Init();

  /* USER CODE BEGIN 2 */

  // Calcula e inicia a onda via DMA (Roda no hardware, livre da CPU!)
  for (i = 0; i < 250; i++){
      seno[i] = (uint16_t)(((sin((double)i * (M_PI * 2.0) / 250.0)) + 1.0) * 100.0);
  }
  HAL_TIM_PWM_Start_DMA(&htim3, TIM_CHANNEL_1, (uint32_t *)seno, 250);

  // Mensagem inicial
  char msg_inicio[] = "Gerador Senoidal Iniciado. Digite a freq (10 a 120 Hz):\r\n";
  HAL_UART_Transmit(&huart1, (uint8_t*)msg_inicio, strlen(msg_inicio), 100);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
      // Fica lendo a porta Serial de forma direta (Polling). Sem travar a onda!
      if (HAL_UART_Receive(&huart1, &rx_data, 1, 10) == HAL_OK) {

          // Eco: Escreve na tela a letra que você acabou de apertar
          HAL_UART_Transmit(&huart1, &rx_data, 1, 10);

          // Se você apertar Enter (\n ou \r)
          if (rx_data == '\n' || rx_data == '\r') {
              if (rx_index > 0) {
                  rx_buffer[rx_index] = '\0';
                  int nova_freq = atoi(rx_buffer);
                  char msg_retorno[50];

                  if (nova_freq >= 10 && nova_freq <= 120) {
                      uint32_t novo_psc = (1200 / nova_freq) - 1;
                      __HAL_TIM_SET_PRESCALER(&htim3, novo_psc);
                      sprintf(msg_retorno, "\r\n=> Sucesso! Freq atualizada: %d Hz\r\n\n", nova_freq);
                  } else {
                      sprintf(msg_retorno, "\r\n=> ERRO: Limite (10-120)\r\n\n");
                  }

                  HAL_UART_Transmit(&huart1, (uint8_t*)msg_retorno, strlen(msg_retorno), 100);
                  rx_index = 0; // Limpa para a próxima digitação
              }
          } else {
              // Se for um número, guarda na memória
              if (rx_index < 9 && rx_data >= '0' && rx_data <= '9') {
                  rx_buffer[rx_index++] = rx_data;
              }
          }
      }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

